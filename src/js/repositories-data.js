    searchRepositories: function(query) {
        const allRepos = this.getAllRepositories();
        const lowercaseQuery = query.toLowerCase();
        
        return allRepos.filter(repo => 
            repo.name.toLowerCase().includes(lowercaseQuery) ||
            repo.description.toLowerCase().includes(lowercaseQuery) ||
            repo.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery)) ||
            repo.language.toLowerCase().includes(lowercaseQuery)
        );
    },
    
    // Get repositories by tags
